# Introduction

print(f"Addition: {3 + 2}")    # addition
print(f"Subtraction: {3 - 2}")    # subtraction
print(f"Multiplication: {3 * 2}")    # multiplication
print(f"Division: {3 / 2}")    # division
print(f"Exponent: {3 ** 2}")   # exponent
print(f"Modulus: {3 % 2}")    # modulus
print(f"Floor division: {3 // 2}")   # floor division operator

# Checking Data Types

print(type(10))                     # int
print(type(3.14))                   # float
print(type(1 + 3j))                 # complex
print(type('Sarvesh'))              # str
print(type([1, 2, 3]))              # list
print(type({'name': 'Sarvesh'}))    # dict
print(type({9.8, 3.14, 2.7}))       # set
print(type((9.8, 3.14, 2.7)))       # tuple
print(type(3 == 3))                 # Bool (True)
print(type(3 >= 3))                 # Bool (False)